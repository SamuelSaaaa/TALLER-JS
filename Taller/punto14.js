// math.js

/**
 * Suma dos números.
 * @param {number} a 
 * @param {number} b 
 @returns {number} 

export function sumar(a, b) {
  return a + b;
}

/**
 * Resta el segundo número del primero.
 * @param {number} a El primer número.
 * @param {number} b El segundo número.
 * @returns {number} La diferencia de a y b.
 */
export function restar(a, b) {
    return a - b;
}